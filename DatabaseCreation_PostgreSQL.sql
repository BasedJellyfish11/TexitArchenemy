-- ---- Credentials ----

CREATE OR REPLACE FUNCTION get_discord_creds()
RETURNS TABLE(token VARCHAR(60))
LANGUAGE plpgsql AS $$
BEGIN
    RETURN QUERY SELECT d.token FROM discord_auth d LIMIT 1;
END;
$$;

CREATE OR REPLACE FUNCTION get_twitter_creds()
RETURNS TABLE(api_key VARCHAR(25), api_secret VARCHAR(50), api_token VARCHAR(116))
LANGUAGE plpgsql AS $$
BEGIN
    RETURN QUERY SELECT t.api_key, t.api_secret, t.api_token FROM twitter_auth t LIMIT 1;
END;
$$;

-- ---- Repost checking ----

-- Returns (channel_id, message_id) of existing repost, or ('-1', '-1') if not a repost (and inserts it).
CREATE OR REPLACE FUNCTION check_repost(
    p_channel_id          VARCHAR(20),
    p_message_id          VARCHAR(20),
    p_link_id             VARCHAR(20),
    p_link_type_description VARCHAR(20)
)
RETURNS TABLE(channel_id VARCHAR(20), message_id VARCHAR(20))
LANGUAGE plpgsql AS $$
DECLARE
    v_count INT;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM repost_repository r
    INNER JOIN link_types lt ON r.link_type_id = lt.link_type_id
    WHERE r.channel_id              = p_channel_id
      AND r.link_id                 = p_link_id
      AND lt.link_type_description  = p_link_type_description;

    IF v_count > 0 THEN
        RETURN QUERY
            SELECT r.channel_id, r.message_id
            FROM repost_repository r
            INNER JOIN link_types lt ON r.link_type_id = lt.link_type_id
            WHERE r.channel_id              = p_channel_id
              AND r.link_id                 = p_link_id
              AND lt.link_type_description  = p_link_type_description;
    ELSE
        INSERT INTO repost_repository (channel_id, message_id, link_id, link_type_id)
        VALUES (
            p_channel_id,
            p_message_id,
            p_link_id,
            (SELECT lt.link_type_id FROM link_types lt WHERE lt.link_type_description = p_link_type_description)
        );
        RETURN QUERY SELECT '-1'::VARCHAR(20), '-1'::VARCHAR(20);
    END IF;
END;
$$;

CREATE OR REPLACE FUNCTION preemptive_repost_check(
    p_channel_id            VARCHAR(20),
    p_link_id               VARCHAR(20),
    p_link_type_description VARCHAR(20)
)
RETURNS TABLE(repost_number BIGINT)
LANGUAGE plpgsql AS $$
BEGIN
    RETURN QUERY
        SELECT COUNT(*)
        FROM repost_repository r
        INNER JOIN link_types lt ON r.link_type_id = lt.link_type_id
        WHERE r.channel_id              = p_channel_id
          AND r.link_id                 = p_link_id
          AND lt.link_type_description  = p_link_type_description;
END;
$$;

CREATE OR REPLACE FUNCTION is_repost_channel(p_channel_id VARCHAR(20))
RETURNS TABLE(repost_check BOOLEAN)
LANGUAGE plpgsql AS $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM discord_channels dc WHERE dc.channel_id = p_channel_id) THEN
        RETURN QUERY SELECT FALSE;
    ELSE
        RETURN QUERY
            SELECT dc.repost_check
            FROM discord_channels dc
            WHERE dc.channel_id = p_channel_id;
    END IF;
END;
$$;

