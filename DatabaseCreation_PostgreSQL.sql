CREATE OR REPLACE FUNCTION get_twitter_rule_channels(p_tag INT)
RETURNS TABLE(channel_id VARCHAR(20))
LANGUAGE plpgsql AS $$
BEGIN
    RETURN QUERY
        SELECT rcr.channel_id
        FROM rule_channel_relation rcr
        WHERE rcr.tag = p_tag;
END;
$$;

-- Returns (tag, added): tag=-1 and added=false means the channel<->rule pair already existed.
CREATE OR REPLACE FUNCTION add_twitter_rule(
    p_channel_id VARCHAR(20),
    p_guild_id   VARCHAR(20),
    p_rule_value VARCHAR(512)
)
RETURNS TABLE(tag INT, added BOOLEAN)
LANGUAGE plpgsql AS $$
DECLARE
    v_tag           INT;
    v_actually_added BOOLEAN := FALSE;
BEGIN
    IF NOT EXISTS (SELECT 1 FROM discord_channels dc WHERE dc.channel_id = p_channel_id) THEN
        INSERT INTO discord_channels (channel_id, guild_id) VALUES (p_channel_id, p_guild_id);
    END IF;

    IF NOT EXISTS (SELECT 1 FROM twitter_rules tr WHERE tr.rule_value = p_rule_value) THEN
        INSERT INTO twitter_rules (rule_value) VALUES (p_rule_value);
        v_actually_added := TRUE;
    END IF;

    SELECT tr.tag INTO v_tag FROM twitter_rules tr WHERE tr.rule_value = p_rule_value;

    BEGIN
        INSERT INTO rule_channel_relation (tag, channel_id) VALUES (v_tag, p_channel_id);
        RETURN QUERY SELECT v_tag, v_actually_added;
    EXCEPTION WHEN unique_violation OR foreign_key_violation THEN
        RETURN QUERY SELECT -1::INT, FALSE::BOOLEAN;
    END;
END;
$$;

