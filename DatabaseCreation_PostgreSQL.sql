-- T-SQL used a VALUES table trick for GREATEST(x, 0). PostgreSQL has GREATEST() natively.
CREATE OR REPLACE FUNCTION update_box_challenge_progress(
    p_user_id    VARCHAR(20),
    p_boxes_drawn INT
)
RETURNS TABLE(boxes_drawn INT)
LANGUAGE plpgsql AS $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM draw_a_box_box_challenge dbc WHERE dbc.user_id = p_user_id) THEN
        INSERT INTO draw_a_box_box_challenge (user_id, boxes_drawn)
        VALUES (p_user_id, GREATEST(p_boxes_drawn, 0));
    ELSE
        UPDATE draw_a_box_box_challenge dbc
        SET boxes_drawn = GREATEST(dbc.boxes_drawn + p_boxes_drawn, 0)
        WHERE dbc.user_id = p_user_id;
    END IF;

    RETURN QUERY
        SELECT dbc.boxes_drawn
        FROM draw_a_box_box_challenge dbc
        WHERE dbc.user_id = p_user_id;
END;
$$;

CREATE OR REPLACE FUNCTION get_box_challenge_progress(p_user_id VARCHAR(20))
RETURNS TABLE(boxes_drawn INT)
LANGUAGE plpgsql AS $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM draw_a_box_box_challenge dbc WHERE dbc.user_id = p_user_id) THEN
        RETURN QUERY SELECT 0::INT;
    ELSE
        RETURN QUERY
            SELECT dbc.boxes_drawn
            FROM draw_a_box_box_challenge dbc
            WHERE dbc.user_id = p_user_id;
    END IF;
END;
$$;


-- ============================================================
-- PROCEDURES  (void operations — no result set returned)
-- Called from C# as: CALL proc_name(@p1, @p2)
-- ============================================================

CREATE OR REPLACE PROCEDURE mark_as_repost_channel(
    p_channel_id VARCHAR(20),
    p_guild_id   VARCHAR(20)
)
LANGUAGE plpgsql AS $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM discord_channels dc WHERE dc.channel_id = p_channel_id) THEN
        INSERT INTO discord_channels (channel_id, channel_type_id, guild_id, pixiv_expand, repost_check)
        VALUES (p_channel_id, 1, p_guild_id, FALSE, TRUE);
    ELSE
        UPDATE discord_channels
        SET repost_check = TRUE
        WHERE channel_id = p_channel_id;
    END IF;
END;
$$;

CREATE OR REPLACE PROCEDURE delete_twitter_rule(p_tag INT, p_channel_id VARCHAR(20))
LANGUAGE plpgsql AS $$
BEGIN
    DELETE FROM rule_channel_relation
    WHERE tag = p_tag AND channel_id = p_channel_id;
END;
$$;

-- Internal helper called by trigger functions below
CREATE OR REPLACE PROCEDURE remove_useless_channels()
LANGUAGE plpgsql AS $$
BEGIN
    DELETE FROM discord_channels
    WHERE pixiv_expand  = FALSE
      AND repost_check  = FALSE
      AND channel_id NOT IN (SELECT rcr.channel_id FROM rule_channel_relation rcr);
END;
$$;


-- ============================================================
-- TRIGGERS
--
-- PostgreSQL triggers require a separate trigger function
-- (RETURNS TRIGGER) plus a CREATE TRIGGER statement.
-- ============================================================

-- Cleans up stored reposts when repost_check is turned off for a channel.
CREATE OR REPLACE FUNCTION fn_repost_cleanup() RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    -- Only act when repost_check transitions from TRUE to FALSE
    IF OLD.repost_check = TRUE AND NEW.repost_check = FALSE THEN
        DELETE FROM repost_repository WHERE channel_id = NEW.channel_id;
    END IF;
    RETURN NULL;
END;
$$;

CREATE TRIGGER repost_cleanup
AFTER UPDATE ON discord_channels
FOR EACH ROW EXECUTE FUNCTION fn_repost_cleanup();


-- Removes channels that are no longer useful after an update.
CREATE OR REPLACE FUNCTION fn_channel_cleanup_after_update() RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    CALL remove_useless_channels();
    RETURN NULL;
END;
$$;

CREATE TRIGGER channel_cleanup
AFTER UPDATE ON discord_channels
FOR EACH ROW EXECUTE FUNCTION fn_channel_cleanup_after_update();


-- Removes orphan twitter_rules (rules with no channel associations) after a rule-channel link is deleted.
CREATE OR REPLACE FUNCTION fn_rule_cleanup() RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    DELETE FROM twitter_rules
    WHERE tag NOT IN (SELECT rcr.tag FROM rule_channel_relation rcr);
    RETURN NULL;
END;
$$;

CREATE TRIGGER rule_cleanup
AFTER DELETE ON rule_channel_relation
FOR EACH ROW EXECUTE FUNCTION fn_rule_cleanup();


-- Removes channels made useless after a rule-channel link is deleted.
CREATE OR REPLACE FUNCTION fn_channel_cleanup_after_relation_delete() RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    CALL remove_useless_channels();
    RETURN NULL;
END;
$$;

CREATE TRIGGER channel_cleanup_relation
AFTER DELETE ON rule_channel_relation
FOR EACH ROW EXECUTE FUNCTION fn_channel_cleanup_after_relation_delete();
