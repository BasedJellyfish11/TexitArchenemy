-- GENERATED BY DEFAULT (not ALWAYS) lets us INSERT explicit IDs during data migration.
-- After migrating data, reset the sequence (see migration guide).
CREATE TABLE channel_types (
    channel_type_id          INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    channel_type_description VARCHAR(10) NOT NULL
);
INSERT INTO channel_types (channel_type_description) VALUES ('text'), ('voice');

-- BIT -> BOOLEAN, DEFAULT 0 -> DEFAULT FALSE
CREATE TABLE discord_channels (
    channel_id      VARCHAR(20) PRIMARY KEY,
    guild_id        VARCHAR(20) NOT NULL,
    channel_type_id INT         NOT NULL DEFAULT 1,
    repost_check    BOOLEAN     NOT NULL DEFAULT FALSE,
    pixiv_expand    BOOLEAN     NOT NULL DEFAULT FALSE,
    FOREIGN KEY (channel_type_id) REFERENCES channel_types(channel_type_id)
);

CREATE TABLE twitter_rules (
    tag        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    rule_value VARCHAR(512) UNIQUE NOT NULL
);

CREATE TABLE rule_channel_relation (
    tag        INT         NOT NULL,
    channel_id VARCHAR(20) NOT NULL,
    PRIMARY KEY (tag, channel_id),
    FOREIGN KEY (tag)        REFERENCES twitter_rules(tag)       ON DELETE CASCADE,
    FOREIGN KEY (channel_id) REFERENCES discord_channels(channel_id) ON DELETE CASCADE
);

CREATE TABLE link_types (
    link_type_id          INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    link_type_description VARCHAR(10) NOT NULL UNIQUE
);
INSERT INTO link_types (link_type_description) VALUES ('Twitter'), ('Pixiv'), ('Artstation');

CREATE TABLE repost_repository (
    message_id   VARCHAR(20) NOT NULL,
